    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed
         String naziv = jTextFieldNaziv.getText();
          String isbn = jTextFieldISBN.getText(); 
          int godIzdanja;
         try { 
           godIzdanja = Integer.parseInt(jTextFieldGodinaIzdanja.getText());
         } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Godina izdanja mora biti broj!", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
                return;
          }
         Zanr zanr =   (Zanr) jComboBoxZanr.getSelectedItem();
         Autor odabraniAutor = (Autor) jComboBoxAutori.getSelectedItem();
         Knjiga novaKnjiga = new Knjiga(naziv, odabraniAutor, isbn, godIzdanja,zanr);

         
         kontroler.dodajKnjigu(novaKnjiga);// Controller.getInstance().dodajKnjigu(novaKnjiga);
         gf.osveziTabelu();
         JOptionPane.showMessageDialog(this, "Knjiga je uspe≈°no dodata!", "Uspe≈°no", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        
    }//GEN-LAST:event_jButtonDodajActionPerformed

    private void jButtonIZMENIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIZMENIActionPerformed
        String naziv = jTextFieldNaziv.getText(); 
          int godIzdanja;
         try { 
           godIzdanja = Integer.parseInt(jTextFieldGodinaIzdanja.getText());
         } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Godina izdanja mora biti broj!", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
                return;
          }
         Zanr zanr =   (Zanr) jComboBoxZanr.getSelectedItem();
         Autor odabraniAutor = (Autor) jComboBoxAutori.getSelectedItem();
         
          knjigaZaIzmenu.setAutor(odabraniAutor);
           knjigaZaIzmenu.setNaslov(naziv);
           knjigaZaIzmenu.setGodinaIzdanja(godIzdanja);
           knjigaZaIzmenu.setZanr(zanr);
           gf.osveziTabelu();
           JOptionPane.showMessageDialog(this, "Knjiga je uspe≈°no izmenjena!", "Uspe≈°no", JOptionPane.INFORMATION_MESSAGE);

            this.dispose();
    }//GEN-LAST:event_jButtonIZMENIActionPerformed

    

